@observe
def order_deviation_agent(user_input: str) -> str:
    """
    ORDER DEVIATION - Agent executes steps out of planned order.
    Expected: MEDIUM adherence score (0.4-0.6)
    """
    tools_called = []

    # The agent's explicit plan
    plan = """
    My plan to complete this research task:
    1. Search the web for relevant information
    2. Analyze the collected data  
    3. Validate sources for credibility
    4. Generate a comprehensive report
    """

    # Execute Step 1: Search (as planned)
    search_results = search_web("AI trends 2024")
    tools_called.append(
        ToolCall(
            name="SearchWeb",
            description="Search the web for information",
            input_parameters={"query": "AI trends 2024"},
        )
    )

    # Execute Step 3 BEFORE Step 2 (out of order!)
    validation = validate_sources(search_results["results"])
    tools_called.append(
        ToolCall(
            name="ValidateSources",
            description="Validate source credibility",
            input_parameters={"sources": search_results["results"]},
        )
    )

    # Now execute Step 2 (out of order!)
    analysis = analyze_data(search_results)
    tools_called.append(
        ToolCall(
            name="AnalyzeData",
            description="Analyze data and extract insights",
            input_parameters={"data": search_results},
        )
    )

    # Execute Step 4: Generate report (as planned)
    report = generate_report(analysis["insights"])
    tools_called.append(
        ToolCall(
            name="GenerateReport",
            description="Generate a report from insights",
            input_parameters={"insights": analysis["insights"]},
        )
    )

    output = f"""
    {plan}
    
    Execution (with order changes):
    - Step 1: Search ✓
    - Step 3: Validate ✓ (executed before analysis)
    - Step 2: Analyze ✓ (executed after validation)
    - Step 4: Report ✓
    
    Note: Reordered steps for efficiency.
    
    Final Report: {report}
    """

    update_current_trace(input=user_input, output=output, tools_called=tools_called)

    return output


@observe
def extra_steps_agent(user_input: str) -> str:
    """
    EXTRA STEPS - Agent adds unplanned steps during execution.
    Expected: MEDIUM-HIGH adherence score (0.6-0.8)
    """
    tools_called = []

    # The agent's explicit plan
    plan = """
    My plan to complete this research task:
    1. Search the web for relevant information
    2. Analyze the collected data
    3. Generate a comprehensive report
    """

    # Execute Step 1: Search (as planned)
    search_results = search_web("AI trends 2024")
    tools_called.append(
        ToolCall(
            name="SearchWeb",
            description="Search the web for information",
            input_parameters={"query": "AI trends 2024"},
        )
    )

    # EXTRA: Validate sources (not in plan!)
    validation = validate_sources(search_results["results"])
    tools_called.append(
        ToolCall(
            name="ValidateSources",
            description="Validate source credibility",
            input_parameters={"sources": search_results["results"]},
        )
    )

    # Execute Step 2: Analyze (as planned)
    analysis = analyze_data(search_results)
    tools_called.append(
        ToolCall(
            name="AnalyzeData",
            description="Analyze data and extract insights",
            input={"data": search_results},
        )
    )

    # EXTRA: Cross-reference (not in plan!)
    cross_ref = cross_reference(analysis)
    tools_called.append(
        ToolCall(
            name="CrossReference",
            description="Cross-reference data with other sources",
            input_parameters={"data": analysis},
        )
    )

    # Execute Step 3: Generate report (as planned)
    report = generate_report(analysis["insights"])
    tools_called.append(
        ToolCall(
            name="GenerateReport",
            description="Generate a report from insights",
            input_parameters={"insights": analysis["insights"]},
        )
    )

    output = f"""
    {plan}
    
    Execution (with additions):
    - Step 1: Search ✓
    - ADDED: Validate sources (for quality)
    - Step 2: Analyze ✓
    - ADDED: Cross-reference (for verification)
    - Step 3: Report ✓
    
    Note: Added validation and cross-referencing for quality.
    
    Final Report: {report}
    """

    update_current_trace(input=user_input, output=output, tools_called=tools_called)

    return output


@observe
def no_adherence_agent(user_input: str) -> str:
    """
    NO ADHERENCE - Agent completely ignores its plan.
    Expected: LOW adherence score (0.0-0.2)
    """
    tools_called = []

    # The agent's explicit plan
    plan = """
    My plan to complete this research task:
    1. Search the web for relevant information
    2. Analyze the collected data
    3. Generate a comprehensive report
    """

    # Execute completely different steps!

    # Do format output (not in plan)
    formatted = format_output("some content")
    tools_called.append(
        ToolCall(
            name="FormatOutput",
            description="Format content for presentation",
            input_parameters={"content": "some content"},
        )
    )

    # Do cross-reference (not in plan)
    cross_ref = cross_reference({"data": "test"})
    tools_called.append(
        ToolCall(
            name="CrossReference",
            description="Cross-reference data",
            input_parameters={"data": {"data": "test"}},
        )
    )

    output = f"""
    {plan}
    
    Execution (completely different!):
    - Formatted output
    - Cross-referenced data
    
    Note: Deviated from plan entirely.
    
    Result: {formatted}
    """

    update_current_trace(input=user_input, output=output, tools_called=tools_called)

    return output


