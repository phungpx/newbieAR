"""
Plan Adherence Metric Demonstration

This script demonstrates the Plan Adherence metric using a planning agent.
The metric evaluates whether the agent follows the plan it created during
task execution, comparing planned steps against actual execution.

Key Concepts:
- Trace-based metric using @observe decorator
- Extracts plan from agent's reasoning/output
- Compares planned steps with executed actions
- Evaluates order compliance and completeness

Reference: https://deepeval.com/docs/metrics-plan-adherence
"""

from deepeval.tracing import observe, update_current_trace
from deepeval.dataset import Golden, EvaluationDataset
from deepeval.metrics import PlanAdherenceMetric
from deepeval.test_case import ToolCall
from deepeval.models.llms import LocalModel
from settings import ProjectSettings

# Initialize settings and model
settings = ProjectSettings()

model = LocalModel(
    model=settings.llm_model,
    api_key=settings.llm_api_key,
    base_url=settings.llm_base_url,
    temperature=settings.llm_temperature,
)


# =============================================================================
# Simulated Tools
# =============================================================================


def search_web(query: str) -> dict:
    """Search the web for information."""
    return {"results": [f"Result for: {query}"], "count": 5}


def analyze_data(data: dict) -> dict:
    """Analyze data and extract insights."""
    return {"insights": ["Key finding 1", "Key finding 2"], "confidence": 0.85}


def generate_report(insights: list) -> str:
    """Generate a report from insights."""
    return f"Report based on {len(insights)} insights"


def validate_sources(sources: list) -> dict:
    """Validate the credibility of sources."""
    return {"valid": True, "score": 0.9}


def format_output(content: str) -> str:
    """Format content for presentation."""
    return f"Formatted: {content}"


def cross_reference(data: dict) -> dict:
    """Cross-reference data with other sources."""
    return {"verified": True, "matches": 3}


# =============================================================================
# Agent Implementations - Different Adherence Levels
# =============================================================================


@observe
def perfect_adherence_agent(user_input: str) -> str:
    """
    PERFECT ADHERENCE - Agent follows its plan exactly.
    Expected: HIGH adherence score (1.0)
    """
    tools_called = []

    # The agent's explicit plan (would normally be generated by LLM)
    plan = """
    My plan to complete this research task:
    1. Search the web for relevant information
    2. Analyze the collected data
    3. Generate a comprehensive report
    """

    # Execute Step 1: Search (as planned)
    search_results = search_web("AI trends 2024")
    tools_called.append(
        ToolCall(
            name="SearchWeb",
            description="Search the web for information",
            input_parameters={"query": "AI trends 2024"},
        )
    )

    # Execute Step 2: Analyze (as planned)
    analysis = analyze_data(search_results)
    tools_called.append(
        ToolCall(
            name="AnalyzeData",
            description="Analyze data and extract insights",
            input_parameters={"data": search_results},
        )
    )

    # Execute Step 3: Generate report (as planned)
    report = generate_report(analysis["insights"])
    tools_called.append(
        ToolCall(
            name="GenerateReport",
            description="Generate a report from insights",
            input_parameters={"insights": analysis["insights"]},
        )
    )

    output = f"""
    {plan}
    
    Execution completed following the plan:
    - Step 1: Searched for AI trends 2024 ✓
    - Step 2: Analyzed {len(search_results['results'])} results ✓
    - Step 3: Generated report ✓
    
    Final Report: {report}
    """

    update_current_trace(input=user_input, output=output, tools_called=tools_called)

    return output


@observe
def partial_adherence_agent(user_input: str) -> str:
    """
    PARTIAL ADHERENCE - Agent skips some planned steps.
    Expected: MEDIUM adherence score (0.5-0.7)
    """
    tools_called = []

    # The agent's explicit plan
    plan = """
    My plan to complete this research task:
    1. Search the web for relevant information
    2. Validate sources for credibility
    3. Analyze the collected data
    4. Generate a comprehensive report
    """

    # Execute Step 1: Search (as planned)
    search_results = search_web("AI trends 2024")
