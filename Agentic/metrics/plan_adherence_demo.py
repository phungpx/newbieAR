def main():
    """Run all demonstrations."""
    print("=" * 70)
    print("PLAN ADHERENCE METRIC DEMONSTRATION")
    print("Planning Agent with Different Adherence Levels")
    print("=" * 70)

    print(
        """
    The Plan Adherence metric evaluates whether an agent follows
    the plan it creates. This is crucial for:
    
    - Predictability: Agents should do what they say they'll do
    - Debugging: Identify when/why agents deviate from plans
    - Trust: Users rely on agents behaving consistently
    - Quality: Ensure planning logic is actually used
    
    This demo shows five adherence scenarios from perfect to none.
    """
    )

    # Explain scenarios
    explain_adherence_scenarios()

    # Run evaluations
    results = evaluate_adherence_scenarios()

    # Summary
    print("\n" + "=" * 70)
    print("RESULTS SUMMARY")
    print("=" * 70)

    for name, score in results:
        status = "PASS" if score >= 0.7 else "FAIL"
        bar = "█" * int(score * 20) + "░" * (20 - int(score * 20))
        print(f"\n{name}")
        print(f"  [{bar}] {score:.2f} [{status}]")

    print("\n" + "=" * 70)
    print("KEY TAKEAWAYS")
    print("=" * 70)
    print(
        """
    1. Plan Adherence is a TRACE-BASED metric (requires @observe)
    2. The agent must generate an EXPLICIT plan for evaluation
    3. Score considers both COMPLETENESS and ORDER of steps
    4. Skipped steps reduce score significantly
    5. Extra steps may reduce score (depending on context)
    6. Order changes are penalized but less than skipped steps
    
    Best Practices for High Adherence:
    - Make plans explicit in agent output/reasoning
    - Execute steps in the order they're planned
    - If deviating, update the plan before executing
    - Use chain-of-thought to make planning visible
    
    When Low Adherence is Acceptable:
    - Error recovery (adding retry steps)
    - Adaptive planning (updating based on results)
    - Safety additions (adding validation steps)
    """
    )


if __name__ == "__main__":
    main()
