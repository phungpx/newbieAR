def evaluate_adherence_scenarios():
    """Evaluate all adherence scenarios."""
    print("\n" + "=" * 70)
    print("PLAN ADHERENCE METRIC DEMONSTRATION")
    print("=" * 70)

    metric = PlanAdherenceMetric(
        model=model,
        threshold=0.7,
        include_reason=True,
        verbose_mode=True,
    )

    user_input = "Research and summarize the latest AI trends for 2024"

    agents = [
        ("Perfect Adherence (follows plan exactly)", perfect_adherence_agent),
        ("Partial Adherence (skips some steps)", partial_adherence_agent),
        ("Order Deviation (wrong step order)", order_deviation_agent),
        ("Extra Steps (adds unplanned steps)", extra_steps_agent),
        ("No Adherence (ignores plan)", no_adherence_agent),
    ]

    results = []

    for name, agent_func in agents:
        print(f"\n{'='*60}")
        print(f"SCENARIO: {name}")
        print(f"{'='*60}")

        dataset = EvaluationDataset(goldens=[Golden(input=user_input)])

        for golden in dataset.evals_iterator(metrics=[metric]):
            output = agent_func(golden.input)

            # Show abbreviated output
            print(f"\nUser Input: {golden.input}")
            print(f"\nAgent Plan + Execution (abbreviated):")
            print(output[:500] + "...")

            print(f"\n--- Adherence Evaluation ---")
            print(f"Score: {metric.score:.2f}")
            print(f"Passed: {metric.score >= metric.threshold}")
            print(f"Reason: {metric.reason}")

            results.append((name, metric.score))

    return results


